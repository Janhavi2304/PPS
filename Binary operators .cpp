#include<iostream>
using namespace std;
int main()
{
  int a=5, b=9;
  
  //Addition operator
  cout<<"a+b="<<(a+b)<<endl;
  
  //Substraction operator
  cout<<"a-b="<<(a-b)<<endl;
  
  //Multiplication operator
  cout<<"a*b="<<(a*b)<<endl;
  
  //Division operator
  cout<<"a/b="<<(a/b)<<endl;
  
  //Modulus operator
  cout<<"a%b="<<(a%b)<<endl;
  
  return 0;
}
